/**
 * Interface que define um contrato para operações de pesquisa.
 * Qualquer classe que implementa esta interface deve fornecer uma implementação
 * para o método realizarPesquisa(), que representa a realização de uma pesquisa.
 */
public interface Pesquisa {

    /**
     * Método responsável por realizar uma pesquisa.
     * 
     * Este método deve ser implementado por qualquer classe que implemente esta interface.
     * Ele define o comportamento da realização de uma pesquisa específica.
     */
    void realizarPesquisa();
}
